        public override string[] GetRolesForUser(string username)
        {
            using (var context = new HotelBookingEntities())
            {
                var roles = (from admin in context.admins
                             join userRole in context.admin_role on admin.id equals userRole.admin_id
                             join role in context.roles on userRole.role_id equals role.id
                             where admin.username == username
                             select role.name).ToArray();
                return roles;
            }
        }

